package SP;

public class Leagues {
	
	private int sifra;
	private String name;
	private String commentator_1;
	private String commentator_2;
	private int prizePool;
	
	public int getSifra() {
		return sifra;
	}
	public void setSifra(int sifra) {
		this.sifra = sifra;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getCommentator_1() {
		return commentator_1;
	}
	public void setCommentator_1(String commentator_1) {
		this.commentator_1 = commentator_1;
	}
	public String getCommentator_2() {
		return commentator_2;
	}
	public void setCommentator_2(String commentator_2) {
		this.commentator_2 = commentator_2;
	}
	public int getPrizePool() {
		return prizePool;
	}
	public void setPrizePool(int prizePool) {
		this.prizePool = prizePool;
	}
	

}
